    // Fungsi utama untuk mencari siswa berdasarkan NISN yang diinput
    function searchStudent() {
        const nisn = nisnInput.value.trim(); // Ambil NISN dari input dan hapus spasi awal/akhir
        clearResults(); // Bersihkan hasil pencarian sebelumnya

        // Validasi jika input NISN kosong
        if (!nisn) {
            errorMessageP.textContent = 'Silakan masukkan NISN Anda.';
            errorMessageP.style.display = 'block'; // Tampilkan pesan error
            return; // Hentikan fungsi
        }

        // Cari siswa dalam array allStudents
        // Gunakan toString() untuk memastikan perbandingan string (NISN bisa jadi angka atau string di JSON)
        const foundStudent = allStudents.find(student => student.NISN.toString() === nisn);

        // Jika siswa ditemukan
        if (foundStudent) {
             // Cek kondisi khusus: Keterangan "SILAHKAN MENGHUBUNGI OPERATOR" atau nilai AGM (sebagai sampel) error
            if (foundStudent.KETERANGAN === "SILAHKAN MENGHUBUNGI OPERATOR" || foundStudent.AGM === "#DIV/0!") {
                displayOperatorMessage(foundStudent); // Tampilkan pesan khusus
            } else {
                displayResults(foundStudent); // Tampilkan hasil normal
            }
        } else {
            // Jika siswa tidak ditemukan
            errorMessageP.textContent = `Siswa dengan NISN ${nisn} tidak ditemukan. Pastikan NISN sudah benar.`;
            errorMessageP.style.display = 'block'; // Tampilkan pesan error
        }
    }

